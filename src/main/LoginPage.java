    public void prosesLogin() {
        String username = txtUsernameD.getText();
        String password = txtPasswordD.getText();
        //String loginSebagai = comboBoxLevelD.getSelectedItem().toString();

        if (username.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Kolom username belum diisi!"
                    + "", "Warning", JOptionPane.WARNING_MESSAGE);
            txtUsernameD.requestFocus();
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Kolom password belum diisi!"
                    + "", "Warning", JOptionPane.WARNING_MESSAGE);
            txtPasswordD.requestFocus();
        } else {
            try {
                conn = DBConnection.getConnection();
                pstm = conn.prepareStatement("select * from user where username=? and password=?");
                pstm.setString(1, username);
                pstm.setString(2, password);
                rs = pstm.executeQuery();

                if (rs.next()) {
                    String levelMysql = rs.getString("level");
                    String usernameMysql = rs.getString("username");
                    if (levelMysql.equalsIgnoreCase("administrator")) {
                        JOptionPane.showMessageDialog(null, "Login berhasil!", "Success", JOptionPane.INFORMATION_MESSAGE);
                        AdminPage ap = new AdminPage(username, levelMysql);
                        ap.setVisible(true);
                        setVisible(false);
                    }
                    if (levelMysql.equalsIgnoreCase("tamu")) {
                        JOptionPane.showMessageDialog(null, "Login berhasil!", "Success", JOptionPane.INFORMATION_MESSAGE);
                        GuestPage ht = new GuestPage(username, levelMysql);
                        ht.setVisible(true);
                        setVisible(false);
                    }
                    if (levelMysql.equalsIgnoreCase("Resepsionis")) {
                        JOptionPane.showMessageDialog(null, "Login berhasil!", "Success", JOptionPane.INFORMATION_MESSAGE);
                        ResepsionisPage rp = new ResepsionisPage(username, levelMysql);
                        rp.setVisible(true);
                        setVisible(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Usrname atau Password Salah", "Login Error", 1);
                }
            } catch (Exception e) {
                System.out.println("" + e);
            }
        }
    }

